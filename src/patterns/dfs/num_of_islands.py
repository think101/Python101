from typing import List


class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        ones = []
        row, col = len(grid), len(grid[0])
        res = 0

        for i in range(row):
            for j in range(col):
                if grid[i][j] == "1":
                    ones.append((i, j))

        def dfs(i, j):
            grid[i][j] = 0

            if i - 1 >= 0 and grid[i - 1][j] == "1":
                dfs(i - 1, j)
            if i + 1 < row and grid[i + 1][j] == "1":
                dfs(i + 1, j)
            if j - 1 >= 0 and grid[i][j - 1] == "1":
                dfs(i, j - 1)
            if j + 1 < col and grid[i][j + 1] == "1":
                dfs(i, j + 1)

        for (i, j) in ones:
            if grid[i][j] == "1":
                res += 1
                dfs(i, j)

        return res


